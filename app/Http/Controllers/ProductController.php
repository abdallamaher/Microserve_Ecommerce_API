    /**
     * Remove the specified resource from storage.
     *
     * @param  int $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $product = Product::find($id);
        if($product === null ){
            return response([
                'message' => trans('data.null_entity'),
            ], 422);
        }

        $product->delete();

        return response()->json([
            'status' => 'success',
            'message' => trans('data.prod_del'),
        ], 200);
    }
